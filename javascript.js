// Function to show book summary in modal dialog
function showBookSummary() {
    // Get the modal
    var modal = document.getElementById("summaryModal");

    // Display the modal
    modal.style.display = "block";
}

// Function to close the modal
function closeModal() {
    // Get the modal
    var modal = document.getElementById("summaryModal");

    // Hide the modal
    modal.style.display = "none";
}
